package day01;
/**
 * 浮点数：float double ， IEEE 754 标准
 * float：32位浮点数
 * double：64位 
 * double是最常用的浮点数
 * 默认的double类型字面量是double
 * double 后缀D、d
 * float 后缀f、F
 * 浮点数有舍入误差，不能进行精确计算。
 */
public class Demo8浮点数 {

	public static void main(String[] args) {
		double pi=3.1415926535897932d;
		float f=3.1415926535897932f;
		System.out.println(pi);
		System.out.println(f);

	}

}
